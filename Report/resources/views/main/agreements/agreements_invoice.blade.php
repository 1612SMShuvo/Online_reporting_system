        <p>Article VIII.</p>
        <h1>Certificate suspending and withdrawal</h1>
        <p>&nbsp;</p>
        <ol>
        <li><strong> Certificate suspending</strong></li>
        </ol>
        <p><strong>&nbsp;</strong></p>
        <ol>
        <li>Reasons for Certificate suspending:</li>
        </ol>
        <p>Management system Certificate can be suspended to Certified Subject following these facts:</p>
        <ol>
        <li>a) Surveillance/short-notice surveillance audit results show that the Certified Subject does not fulfil all requirements specified in art. VII. Point 12</li>
        <li>b) Non-elimination of serious nonconformities within agreed term (max 60 days) discovered during surveillance/short-notice surveillance audit,</li>
        <li>c) Non-allowance of surveillance audit/ short-notice surveillance performance within the term according to Certification Agreement,</li>
        <li>e) Breaking of information duty &ndash; in communication of changes, which essentially influence management system function,</li>
        <li>f) Misuse of EUROSIA CERTIFICATIONS LTD Certificate or certification mark,</li>
        <li>g) Non-performance of any obligation against Certified Subject &ndash; not even within 30 days after the fulfilling term,</li>
        </ol>
        <p>&nbsp;</p>
        <ol start="2">
        <li>Procedure of Certificate suspending</li>
        </ol>
        <p>In a&nbsp;case of determination of some facts specified in point 1 EUROSIA CERTIFICATIONS LTD shall issue decision on Certificate suspending&ldquo;, where the reasons for Certificate suspending and requirements for Certificate renovation are specified. This decision is delivered to the Certified Subject and the information about certification suspending is published on EUROSIA CERTIFICATIONS LTD web page.</p>
        <p>&nbsp;</p>
        <ol start="3">
        <li>The Certified Subject has no right to use EUROSIA CERTIFICATIONS LTD Certificate and certification mark nor to refer to certification during Certificate suspending. Maximum term for Certificate suspending is 60 days from the publishing of Certificate suspending information on EUROSIA CERTIFICATIONS LTD web page. If the reason for Certificate suspending continues also after the expiration of mentioned term, EUROSIA CERTIFICATIONS LTD shall be obliged to withdraw the Certificate.</li>
        <li>If requirements specified in Decision on Certificate suspending were fulfilled within appointed time, EUROSIA CERTIFICATIONS LTD shall issue Decision on Certificate renovation&ldquo;. This decision is delivered to the Certified Subject and published on EUROSIA CERTIFICATIONS LTD web page.</li>
        </ol>
        <p><strong>&nbsp;</strong></p>
        <ol>
        <li><strong> Certificate withdrawal</strong></li>
        </ol>
        <p>&nbsp;</p>
        <ol>
        <li>Reasons for Certificate withdrawal:</li>
        </ol>
        <p>Management system Certificate withdrawal can be executed following these facts:</p>
        <ol>
        <li>The reason for Certificate suspending was not eliminated within appointed time 60 days,</li>
        <li>Determination that the Certified Subject uses Certificate or refers to it during the Certificate suspending time,</li>
        <li>Certified object is bankrupt or bankruptcy proceedings have been initiated.</li>
        <li>The Certified Subject request.</li>
        <li>Procedure for Certificate withdrawal</li>
        </ol>
        <p>In a&nbsp;case of determination of some facts specified in point 1. EUROSIA CERTIFICATIONS LTD shall issue &bdquo;Decision on Certificate withdrawal&ldquo;, in which the reasons for Certificate withdrawal are specified. This decision is delivered to the Certified Subject and the information about Certificate withdrawal is published on EUROSIA CERTIFICATIONS LTD web page. Certified subject is obliged to return the certificate to the company within 7 days of withdrawal.</p>
