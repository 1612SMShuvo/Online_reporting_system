        <li>The first Surveillance Audit shall be done within 1 Year from the date of certification audit; EUROSIA CERTIFICATIONS LTD shall be obliged to ask the Certified Subject within one month before the expiry of the stated period to enable the first Surveillance Audit,</li>
        <li>The second Surveillance Audit shall be done within 1 year from the termination of the first surveillance Audit; the EUROSIA CERTIFICATIONS LTD shall be obliged to ask the Certified Subject within one month before the expiry of the stated period to enable the second Surveillance Audit.</li>
        </ul>
        <p>Article III.</p>
        <h1>Price of Audit and Payment Terms</h1>
        <p>&nbsp;</p>
        <ol>
        <li>The parties have agreed that the Certified Subject shall pay to the EUROSIA CERTIFICATIONS LTD for the audit a&nbsp;price of BDT. <strong>.50000/=</strong> + Service Tax (ST) at prevailing rates on the date of payment.</li>
        <li>Price for carrying out the audit under point 1 of this article shall consist of the following:</li>
        <li>Price of the Certification Audit has been agreed in the amount of BDT</li>
        <li>Price of the fit BDT Surveillance Audit has been agreed in the amount of BDT</li>
        <li>Price of the second Surveillance Audit has been agreed in the amount of BDT</li>
        <li>The parties have agreed that if it is necessary to carry out an extraordinary Control Audit pursuant to this Agreement, the Certified Subject shall pay, in addition to the price of audits under point 2, to the EUROSIA CERTIFICATIONS LTD the price of each such audit in the amount of BDT.</li>
        </ol>
