        <ul>
        <li>Certification Audit of Management system in compliance with ISO 9001 or ISO 14001 or ISO 45001 or ISO 22000 standards (hereinafter referred to as &ldquo;Certification Audit&ldquo;)</li>
        <li>Arranging the issue of a management system certificate in compliance with ISO 9001 or ISO 14001 or ISO 45001 or ISO 22000 standards (hereinafter referred to also as &ldquo;Certificate&ldquo;)</li>
        <li>Two Surveillance Audits focussed on compliance with conditions of management system according to ISO 9001 or ISO 14001 or ISO 45001 or ISO 22000 standards (hereinafter referred to also as &ldquo;Surveillance Audits&ldquo;)</li>
        </ul>
        <ol start="2">
        <li>Activities under point 1 shall be carried out by the EUROSIA CERTIFICATIONS LTD at the premises of the Certified Subject in&nbsp;the following scope:</li>
        <li>EUROSIA CERTIFICATIONS LTD undertakes, in accordance with the terms and conditions hereof, to carry out activities under point 1 hereof for the Certified Subject (hereinafter referred to as &ldquo;Audit&ldquo;) and the Certified Subject undertakes, in accordance with the terms and conditions hereof, to pay to the EUROSIA CERTIFICATIONS LTD the price of carrying out audit conduction.</li>
        </ol>
        <p>Article II.</p>
        <h1>Place and Time of Audit</h1>
        <ol>
        <li>The audit shall be carried out at the registered office of the Certified Subject, at the Certified Subject&rsquo;s operation or other working places outside the company specified by EUROSIA CERTIFICATIONS LTD.</li>
        <li>This Agreement has been entered into for the period necessary to complete the performance of the subject matter of the Agreement.</li>
        <li>The parties have agreed that the audit shall be carried out as follows:</li>
