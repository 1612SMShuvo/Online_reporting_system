        <p>Article IV</p>
        <p><strong>Terms and Conditions of Performance of the Subject Matter of Agreement</strong></p>
        <ol>
        <li>EUROSIA CERTIFICATIONS LTD shall be obliged to carry out the audit properly, with professional care and at the time agreed herein.</li>
        <li>EUROSIA CERTIFICATIONS LTD shall carry out the audit via:</li>
        </ol>
        <p>Certification Audit</p>
        <ul>
        <li>1<sup>st</sup> stage audit &ndash; documentary (on site)</li>
        <li>2<sup>nd</sup> stage audit (audit conduction on site)</li>
        </ul>
        <p>The first Surveillance Audit 12 months after certification audit</p>
        <p>The second Surveillance Audit 12 months after 1<sup>st</sup> Surveillance audit</p>
        <p>Article V</p>
        <p><strong>Performance of Agreement</strong></p>
        <ol>
        <li>EUROSIA CERTIFICATIONS LTD shall carry out the audit in the following stages:</li>
        </ol>
        <ol>
        <li>certification audit</li>
        </ol>
        <p>1st stage audit &ndash; it consists of management system open issues solving, review of management systems documentation, planning of 2nd stage audit conduction</p>
        <p>2nd stage audit &ndash; audit execution, review of management system documents implementation to use and its effectiveness, pointing out the strengths and improvement opportunities and elaboration of audit report</p>
        <ol>
        <li>the first surveillance audit &ndash; shall be carried out by EUROSIA CERTIFICATIONS LTD auditor for the purpose of the management system optimizing within one year from the day of award of Certificate; in the first Surveillance Audit EUROSIA CERTIFICATIONS LTD Auditor shall assess effectiveness of the management system; EUROSIA CERTIFICATIONS LTD Auditor shall elaborate an audit report of the first Surveillance Audit.</li>
        <li>the second surveillance audit &ndash; shall be carried out by EUROSIA CERTIFICATIONS LTD Auditor for the purpose of the management system optimizing within one year from the completion of the first Surveillance Audit; in the second Surveillance Audit EUROSIA CERTIFICATIONS LTD Auditor shall assess the effectiveness of the management system; EUROSIA CERTIFICATIONS LTD Auditor shall elaborate an audit report of the second Surveillance Audit.
        <ol start="2">
        <li>After completion of the Certification Audit, the EUROSIA CERTIFICATIONS LTD Auditor shall submit to EUROSIA CERTIFICATIONS LTD his/her report with other documents necessary for the final decision. Subsequently, EUROSIA CERTIFICATIONS LTD shall make a decision on the Certificate award.</li>
        <li>In a case that discovered non-conformances documented in the audit report of one of the audits carried out hereunder do not permit the issue of the Certificate, the Certified Subject shall be obliged to eliminate the discovered nonconformities within an agreed period of time. The Certified Subject must notify the EUROSIA CERTIFICATIONS LTD in writing of the way of their elimination. EUROSIA CERTIFICATIONS LTD shall conduct the short-notice surveillance audit to verify the elimination of detected nonconformities, if required. After receiving a&nbsp;written statement about the elimination of the non-conformances, the EUROSIA CERTIFICATIONS LTD Auditor shall verify the elimination of the non-conformances and inform EUROSIA CERTIFICATIONS LTD.</li>
        </ol>
        </li>
        </ol>
        <p>Article VI.</p>
        <h3>Non-disclosure and Confidential Information Protection</h3>
        <ol>
        <li>The EUROSIA CERTIFICATIONS LTD shall be obliged to keep in strict confidence all and any information regarding the subject matter of the present Agreement as well as facts that the EUROSIA CERTIFICATIONS LTD discovers during its performance.</li>
        <li>The EUROSIA CERTIFICATIONS LTD shall be obliged to protect and not to disclose any confidential information regarding the Certified Subject that it acquires during the carrying out of the audit. Confidential information means any information and facts constituting trade secret, business plans and any other information and facts about the Certified Subject that are not generally known.</li>
        <li>The Certified Subject may revoke the non-disclosure obligation of the EUROSIA CERTIFICATIONS LTD. The approval must be in writing.</li>
        <li>The non-disclosure obligation shall equally apply to</li>
        <li>The EUROSIA CERTIFICATIONS LTD&rsquo;s employees,</li>
        <li>Other persons whose services are used by the EUROSIA CERTIFICATIONS LTD during the audit.</li>
        <li>The non-disclosure obligation shall not apply in the event that the purpose of disclosing such information is to prevent the commission of a&nbsp;crime imposed by law. EUROSIA CERTIFICATIONS LTD can disclose the information if its required by or under any Bangladesh law or by courts.</li>
        <li>The Certified Subject shall be obliged not to disclose the content of the present Agreement and the facts that it discovers in connection with the acts by the EUROSIA CERTIFICATIONS LTD during the performance of the subject matter hereof and which represent at the same time a&nbsp;trade secret, and facts that the EUROSIA CERTIFICATIONS LTD expressly determines as facts that are not to be disclosed.</li>
        <li>The non-disclosure and confidential information protection obligation survives even after termination of the present Agreement until the moment when the information in question becomes generally known.</li>
        </ol>
        <p>Article VII.</p>
        <p><strong>Rights and Obligations of the Parties</strong></p>
        <ol>
        <li>The Certificate may only be awarded if the Certified Subject complies with the requirements of respective technical standard, and in the event that the Certified Subject fails to comply with these requirements after the Certificate award, EUROSIA CERTIFICATIONS LTD shall be obliged to withdraw the Certificate.</li>
        <li>The EUROSIA CERTIFICATIONS LTD shall be obliged to notify the Certified Subject of changes in certification procedures that have a&nbsp;direct influence upon it and shall be entitled and obliged to maintain and disclose a&nbsp;list of certified companies together with the area where the Certificate is valid.</li>
        <li>Appeal against an act by EUROSIA CERTIFICATIONS LTD</li>
        </ol>
        <ol>
        <li>In the event that EUROSIA CERTIFICATIONS LTD</li>
        </ol>
        <ul>
        <li>does not issue a&nbsp;Certificate to the Certified Subject, or</li>
        <li>withdraws the Certificate,</li>
        </ul>
