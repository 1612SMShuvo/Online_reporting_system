        <ol>
        <li>In a case that the Certified Subject does not invoke its right to appeal against the act by EUROSIA CERTIFICATIONS LTD stated in a), it shall be considered that it agrees with that act.</li>
        <li>The notice of appeal must be in writing and addressed to the EUROSIA CERTIFICATIONS LTD. In the notice of appeal the Certified Subject shall indicate the following:</li>
        </ol>
        <ul>
        <li>denomination &ldquo;appeal&ldquo;</li>
        <li>number and entering date of the Management System Certification Agreement</li>
        <li>act of EUROSIA CERTIFICATIONS LTD that it appeals against</li>
        <li>reasons why it considers the act by EUROSIA CERTIFICATIONS LTD to be incorrect</li>
        </ul>
        <ol>
        <li>The EUROSIA CERTIFICATIONS LTD Board of Directors shall be obliged to consider each appeal served by the Certified Subject directed to it.</li>
        <li>In the event that the Certified Subject serves a&nbsp;notice of appeal after the time limit stated in a), the Board of Directors shall reject its appeal due to late submission. In serious cases deserving special consideration the Board of Directors shall be entitled to ignore the fact that notice of appeal was not served on time.</li>
        <li>The Certified Subject shall be informed about its appeal in writing not later than 60 days from the day of delivery of the appeal to EUROSIA CERTIFICATIONS LTD. In the event that the EUROSIA CERTIFICATIONS LTD Board of Directors does not make a&nbsp;decision regarding the appeal within this period of time and does not send the decision on the appeal to the Certified Subject, it shall be considered that the appeal by the Certified Subject was &ldquo;satisfactory&ldquo;.
        <ol start="4">
        <li>The Certificate shall become valid on the day of its issue and shall remain&nbsp;valid for 3 years. In order to keep the Certificate valid during the three years, it is necessary to carry out the first and the second Surveillance Audit of the Certified Subject at the time specified herein and with a&nbsp;result justifying validity of the Certificate. In justified cases EUROSIA CERTIFICATIONS LTD may order a short-notice surveillance audit.</li>
        <li>Minimum 2 weeks before audit conduction the Certified Subject shall be obliged to send to EUROSIA CERTIFICATIONS LTD all documents related to the management system, which are requested by EUROSIA CERTIFICATIONS LTD (e.g. quality manual, other documented procedures, register of environmental aspects, risks analysis, management system review report, etc.).</li>
        <li>Before the Certification Audit the Certified Subject shall be obliged to carry out a&nbsp;complete internal audit of the company (all management system processes must be audited) and the management system review in the scope where the audit shall be carried out in compliance with article I., section 2 hereof.</li>
        <li>The Certified Subject shall be obliged to enable the team of auditors of the EUROSIA CERTIFICATIONS LTD to inspect the records regarding the area of validity of the management system, access to organizational divisions that the certification applies to, and to provide all necessary cooperation that the EUROSIA CERTIFICATIONS LTD may ask for. Cooperation provision by the Certified Subject according to points 5, 6 and 7 is necessary condition for audit conducting and breaking of this duty is considered to be essential Agreement breach.</li>
        <li>In a&nbsp;case of any reasons of the Certified Subject, the audit conduction on the place specified by EUROSIA CERTIFICATIONS LTD is not possible, the Certified Subject shall be obligatory to inform EUROSIA CERTIFICATIONS LTD immediately. Possible additional costs, which can arose to EUROSIA CERTIFICATIONS LTD and are related to impossibility of agreed audit conducting at the term and place specified by EUROSIA CERTIFICATIONS LTD, the Certified Subject is obligatory to pay.</li>
        <li>After Certificate award the Certified Subject shall be obliged to inform EUROSIA CERTIFICATIONS LTD about all substantial changes in organization structure, which can essentially influence the management system.</li>
        <li>For the purpose of EUROSIA CERTIFICATIONS LTD accreditation, the Certified Subject agrees that in case of need it will cooperate in a&nbsp;witness assessment (i.e. participation of an accreditation body, EUROSIA CERTIFICATIONS LTD representative, or a respective supervising authority at the Certification/Surveillance Audit) or enables accreditation body, within its supervision of EUROSIA CERTIFICATIONS LTD, to examine the EUROSIA CERTIFICATIONS LTD records about the Certified Subject. For this purpose the Certified Subject agrees with the publication of confidential information to the accreditation body. Publication of confidential information by the accreditation body is not considered to be the Agreement breach according to article VI. of this Agreement.</li>
        <li>The Certified Subject shall be obliged, after the awarding of the management system certificate, to comply with all requirements stated herein and in N&deg; EUROSIA CERTIFICATIONS LTD-AF -10 Use of Marks &amp; Logos contracts/instructions hereto, as well as with requirements arising from respective technical standards or&nbsp;determined during the audit conduction, and to immediately notify EUROSIA CERTIFICATIONS LTD&nbsp;of&nbsp;the failure to comply with them (if any).</li>
